public class Main {
	public static void main(String[] args) {
		//operator perbandingan (== , !=)
		
//operator perbandingan == atau sama dengan
		int a = 8;
		int b = 5;
		System.out.println("angka 8 dan 5, == (sama dengan) hasilnya :  " + (a==b));
		
		// operator perbandingan != atau tidak sama dengan 
		int y = 8;
		int d = 3;
		System.out.println("angka 8 dan 3, != (tidak sama dengan ) hasilnya : " + (y != d));
	}
}
